function splitNameParts(name: string): string[] {
  const trimmed = name.trim();
  if (trimmed.includes(" ")) return trimmed.split(/\s+/);
  if (trimmed.includes("@")) {
    // Extract the local part of an email and treat dot/underscore as word separators
    const local = trimmed.split("@")[0];
    const parts = local.split(/[._-]+/).filter(Boolean).map(p => p.charAt(0).toUpperCase() + p.slice(1).toLowerCase());
    return parts.length > 0 ? parts : [local];
  }
  const camel = trimmed.replace(/([a-z\u00e0-\u00ff])([A-Z\u00c0-\u00df])/g, "$1 $2");
  if (camel !== trimmed) return camel.split(" ");
  return [trimmed];
}

function buildShortNameMap(names: string[]): Map<string, string> {
  const normalize = (name: string) => splitNameParts(name).join(" ");
  const unique = Array.from(new Set(names.filter(Boolean)));

  // Group all variants of the same person under one canonical form
  const canonicalToOriginals = new Map<string, string[]>();
  for (const name of unique) {
    const canonical = normalize(name);
    const group = canonicalToOriginals.get(canonical) ?? [];
    group.push(name);
    canonicalToOriginals.set(canonical, group);
  }

  // Count first names using deduplicated canonicals only
  const firstNameCount = new Map<string, number>();
  for (const canonical of canonicalToOriginals.keys()) {
    const first = canonical.split(" ")[0];
    if (first) firstNameCount.set(first, (firstNameCount.get(first) ?? 0) + 1);
  }

  // Map every variant to its short form
  const map = new Map<string, string>();
  for (const [canonical, originals] of canonicalToOriginals) {
    const parts = canonical.split(" ");
    const first = parts[0];
    let shortForm = first;
    if (!first) {
      shortForm = canonical;
    } else if ((firstNameCount.get(first) ?? 0) > 1) {
      const lastInitial = parts[1]?.[0]?.toUpperCase();
      shortForm = lastInitial ? `${first} ${lastInitial}` : first;
    }
    for (const orig of originals) map.set(orig, shortForm);
  }
  return map;
}

function getTicketCreator(ticket: { nextStepsHistory?: Array<{ text: string; updatedBy: string; updatedAt: string }> | null }): string | null {
  const history = ticket.nextStepsHistory;
  if (!history?.length) return null;
  const SYSTEM_NAMES = new Set(["External", "System", "Unknown", "Airtable Sync"]);
  const isHuman = (name: string) => !!name && !SYSTEM_NAMES.has(name);
  const created = history.find(e => e.text === "Ticket created");
  if (created?.updatedBy && isHuman(created.updatedBy)) return created.updatedBy;
  const first = history.find(e => isHuman(e.updatedBy));
  return first?.updatedBy ?? null;
}

