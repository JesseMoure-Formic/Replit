// ── Error Boundary for ticket edit dialog ─────────────────────────────────────
class TicketFormErrorBoundary extends Component<{ children: ReactNode }, { error: Error | null }> {
  constructor(props: { children: ReactNode }) {
    super(props);
    this.state = { error: null };
  }
  static getDerivedStateFromError(error: Error) {
    return { error };
  }
  componentDidCatch(error: Error, info: ErrorInfo) {
    console.error("[TicketForm crash]", error, info);
  }
  render() {
    if (this.state.error) {
      return (
        <div className="p-6 space-y-3">
          <p className="text-sm font-medium text-destructive">Failed to load ticket form</p>
          <p className="text-xs text-muted-foreground font-mono break-all">{this.state.error.message}</p>
          <button
            onClick={() => this.setState({ error: null })}
            className="text-xs underline text-muted-foreground hover:text-foreground"
          >
            Try again
          </button>
        </div>
      );
    }
    return this.props.children;
  }
}

// ── Customer Directory Dialog ─────────────────────────────────────────────────
function CustomerDirectoryDialog({
  open,
  onOpenChange,
  customerContacts,
}: {
  open: boolean;
  onOpenChange: (v: boolean) => void;
  customerContacts: Record<string, { name: string; email: string | null; phone: string | null }[]>;
}) {
  const [search, setSearch] = useState("");
  const [copied, setCopied] = useState(false);

  const entries = useMemo(() => {
    const q = search.toLowerCase().trim();
    return Object.entries(customerContacts)
      .filter(([customer, contacts]) => {
        if (!q) return true;
        if (customer.toLowerCase().includes(q)) return true;
        return contacts.some(c =>
          c.name.toLowerCase().includes(q) ||
          (c.email ?? "").toLowerCase().includes(q) ||
          (c.phone ?? "").toLowerCase().includes(q)
        );
      })
      .sort(([a], [b]) => a.localeCompare(b));
  }, [customerContacts, search]);

  const handleCopyAll = () => {
    const lines: string[] = [];
    entries.forEach(([customer, contacts]) => {
      lines.push(customer);
      contacts.forEach(c => {
        const parts = [c.name, c.email, c.phone].filter(Boolean);
        lines.push(`  ${parts.join(" | ")}`);
      });
    });
    navigator.clipboard.writeText(lines.join("\n")).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    });
  };

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="sm:max-w-[540px] max-h-[80vh] flex flex-col">
        <DialogHeader>
          <div className="flex items-center justify-between gap-3 pr-6">
            <DialogTitle className="flex items-center gap-2">
              <Building2 className="h-4 w-4 text-[#FF9100]" />
              Customer Directory
            </DialogTitle>
            <Button
              size="sm"
              variant="outline"
              className="h-7 text-xs gap-1.5 shrink-0"
              onClick={handleCopyAll}
              data-testid="btn-copy-directory"
            >
              {copied ? <CopyCheck className="h-3.5 w-3.5 text-green-500" /> : <Copy className="h-3.5 w-3.5" />}
              {copied ? "Copied!" : "Copy all"}
            </Button>
          </div>
        </DialogHeader>

        <div className="px-1 pt-1 pb-2">
          <Input
            placeholder="Search customers, contacts, email, or phone…"
            value={search}
            onChange={e => setSearch(e.target.value)}
            className="h-8 text-sm"
            data-testid="input-directory-search"
            autoFocus
          />
        </div>

        <div className="flex-1 overflow-y-auto pr-1 space-y-1 min-h-0">
          {entries.length === 0 && (
            <p className="text-sm text-muted-foreground text-center py-8">No matches found.</p>
          )}
          {entries.map(([customer, contacts]) => (
            <div key={customer} className="rounded-lg border border-border/50 bg-muted/20 px-3 py-2.5">
              <p
                className="text-sm font-semibold text-foreground select-text cursor-text mb-1.5"
                data-testid={`directory-customer-${customer}`}
              >
                {customer}
              </p>
              {contacts.length > 0 ? (
                <div className="space-y-1">
                  {contacts.map((c, i) => (
                    <div key={i} className="flex flex-col gap-0.5 pl-2 border-l-2 border-[#FF9100]/30">
                      <span className="text-xs font-medium text-foreground/90 select-text cursor-text">
                        {c.name}
                      </span>
                      {c.email && (
                        <span className="text-xs text-muted-foreground select-text cursor-text">
                          {c.email}
                        </span>
                      )}
                      {c.phone && (
                        <span className="text-xs text-muted-foreground select-text cursor-text">
                          {c.phone}
                        </span>
                      )}
                    </div>
                  ))}
                </div>
              ) : (
                <p className="text-xs text-muted-foreground/50">No contacts on record</p>
              )}
            </div>
          ))}
        </div>

        <div className="pt-2 border-t border-border/40 text-xs text-muted-foreground">
          {entries.length} customer{entries.length !== 1 ? "s" : ""}
          {search && ` matching "${search}"`}
        </div>
      </DialogContent>
    </Dialog>
  );
}

