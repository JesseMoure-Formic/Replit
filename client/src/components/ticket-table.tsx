export function TicketTable({
  tickets,
  editingTicket: externalEditing,
  onEditingChange,
  onFilteredCountChange,
  initialColumnFilters,
  onColumnFiltersChange,
}: {
  tickets: Ticket[];
  editingTicket?: Ticket | null;
  onEditingChange?: (t: Ticket | null) => void;
  onFilteredCountChange?: (count: number) => void;
  initialColumnFilters?: ColumnFilters;
  onColumnFiltersChange?: (f: ColumnFilters) => void;
}) {
  const [internalEditing, setInternalEditing] = useState<Ticket | null>(null);
  const [copiedLink, setCopiedLink] = useState(false);
  const editingTicket = externalEditing !== undefined ? externalEditing : internalEditing;
  const setEditingTicket = (t: Ticket | null) => {
    setInternalEditing(t);
    onEditingChange?.(t);
  };
  const [sortColumn, setSortColumn] = useState<SortColumn>(null);
  const [sortDirection, setSortDirection] = useState<SortDirection>("asc");
  const [customerFilter, setCustomerFilter] = useState<Set<string>>(
    () => new Set(initialColumnFilters?.customer ?? [])
  );
  const [priorityFilter, setPriorityFilter] = useState<Set<string>>(
    () => new Set(initialColumnFilters?.priority ?? [])
  );
  const [assigneeFilter, setAssigneeFilter] = useState<Set<string>>(
    () => new Set(initialColumnFilters?.assignee ?? [])
  );
  const [changingPriority, setChangingPriority] = useState<Set<number>>(new Set());
  const [togglingSupport, setTogglingSupport] = useState<Set<number>>(new Set());
  const [togglingParts, setTogglingParts] = useState<Set<number>>(new Set());
  const [directoryOpen, setDirectoryOpen] = useState(false);

  const { data: options } = useQuery<TicketOptions>({ queryKey: ["/api/tickets/options"] });
  const queryClient = useQueryClient();
  const { user } = useAuth();
  const loggedInUserName = useMemo(() => {
    if (!user) return "Unknown";
    return [user.firstName, user.lastName].filter(Boolean).join(" ") || "Unknown";
  }, [user]);

  const { data: slackMembers = [] } = useQuery<Array<{ name: string; id: string }>>({
    queryKey: ["/api/slack/members"],
    staleTime: 10 * 60 * 1000,
  });

  const shortNameMap = useMemo(() => {
    const names: string[] = slackMembers.map(m => m.name).filter(Boolean);
    for (const t of tickets) {
      if (t.assigneeName) names.push(t.assigneeName);
      const creator = getTicketCreator(t);
      if (creator) names.push(creator);
    }
    return buildShortNameMap(names);
  }, [tickets, slackMembers]);

  const sn = (name: string | null | undefined): string =>
    name ? (shortNameMap.get(name) ?? splitNameParts(name)[0] ?? name) : "—";

  const isNewTicket = (ticket: Ticket) => {
    if (!ticket.createdAt) return false;
    const age = Date.now() - new Date(ticket.createdAt).getTime();
    return age < 48 * 60 * 60 * 1000;
  };

  const toggleNeedsSupport = async (ticket: Ticket) => {
    if (togglingSupport.has(ticket.id)) return;
    setTogglingSupport(prev => new Set(prev).add(ticket.id));
    try {
      await apiRequest("PATCH", `/api/tickets/${ticket.id}`, { needsSupport: !ticket.needsSupport });
      await queryClient.invalidateQueries({ queryKey: ["/api/tickets"] });
    } finally {
      setTogglingSupport(prev => { const next = new Set(prev); next.delete(ticket.id); return next; });
    }
  };

  const togglePartsNeeded = async (ticket: Ticket) => {
    if (togglingParts.has(ticket.id)) return;
    setTogglingParts(prev => new Set(prev).add(ticket.id));
    try {
      await apiRequest("PATCH", `/api/tickets/${ticket.id}`, { partsNeeded: !ticket.partsNeeded });
      await queryClient.invalidateQueries({ queryKey: ["/api/tickets"] });
    } finally {
      setTogglingParts(prev => { const next = new Set(prev); next.delete(ticket.id); return next; });
    }
  };

  const handlePriorityChange = async (ticket: Ticket, newLabel: string | null) => {
    if (changingPriority.has(ticket.id)) return;
    const fromLabel = ticket.priorityLabel;
    if (fromLabel === newLabel) return;

    setChangingPriority(prev => new Set(prev).add(ticket.id));
    try {
      const historyEntry = {
        text: newLabel
          ? (fromLabel
              ? `Priority changed from "${fromLabel}" to "${newLabel}"`
              : `Priority set to "${newLabel}"`)
          : `Priority cleared (was "${fromLabel}")`,
        updatedBy: loggedInUserName,
        updatedAt: new Date().toISOString(),
      };
      const currentHistory = ticket.nextStepsHistory || [];
      await apiRequest("PATCH", `/api/tickets/${ticket.id}`, {
        priorityLabel: newLabel || null,
        priority: newLabel ? priorityLevelFromLabel(newLabel) : "medium",
        nextStepsHistory: [...currentHistory, historyEntry],
      });
      await queryClient.invalidateQueries({ queryKey: ["/api/tickets"] });
    } finally {
      setChangingPriority(prev => {
        const next = new Set(prev);
        next.delete(ticket.id);
        return next;
      });
    }
  };

  const handleSort = (column: SortColumn) => {
    if (sortColumn === column) {
      setSortDirection(sortDirection === "asc" ? "desc" : "asc");
    } else {
      setSortColumn(column);
      setSortDirection("asc");
    }
  };

  const makeToggle = (col: "customer" | "priority" | "assignee", current: Set<string>, setter: (s: Set<string>) => void) =>
    (value: string) => {
      const next = new Set(current);
      next.has(value) ? next.delete(value) : next.add(value);
      setter(next);
      if (onColumnFiltersChange) {
        onColumnFiltersChange({
          customer: col === "customer" ? [...next] : [...customerFilter],
          priority: col === "priority" ? [...next] : [...priorityFilter],
          assignee: col === "assignee" ? [...next] : [...assigneeFilter],
        });
      }
    };

  const makeClear = (col: "customer" | "priority" | "assignee", setter: (s: Set<string>) => void) =>
    () => {
      setter(new Set());
      if (onColumnFiltersChange) {
        onColumnFiltersChange({
          customer: col === "customer" ? [] : [...customerFilter],
          priority: col === "priority" ? [] : [...priorityFilter],
          assignee: col === "assignee" ? [] : [...assigneeFilter],
        });
      }
    };

  const getOptions = (col: "customer" | "priority" | "assignee"): string[] => {
    const set = new Set<string>();
    tickets.forEach((t) => {
      const val =
        col === "customer" ? t.customerName :
        col === "priority" ? (t.priorityLabel ? shortenPriority(t.priorityLabel) : null) :
        t.assigneeName;
      if (val) set.add(val);
    });
    if (col === "priority") {
      return Array.from(set).sort((a, b) => {
        const aPrefix = a.match(/^([^:]+):/)?.[1].trim() ?? "";
        const bPrefix = b.match(/^([^:]+):/)?.[1].trim() ?? "";
        const aIsFO = aPrefix === "FO";
        const bIsFO = bPrefix === "FO";
        if (aIsFO && !bIsFO) return -1;
        if (!aIsFO && bIsFO) return 1;
        const aPLevel = parseInt(a.match(/P(\d+)/i)?.[1] ?? "99");
        const bPLevel = parseInt(b.match(/P(\d+)/i)?.[1] ?? "99");
        if (aPLevel !== bPLevel) return aPLevel - bPLevel;
        return a.localeCompare(b);
      });
    }
    return Array.from(set).sort();
  };

  const filteredAndSortedTickets = tickets.filter((t) => {
    if (customerFilter.size > 0 && !customerFilter.has(t.customerName || "")) return false;
    if (priorityFilter.size > 0 && !priorityFilter.has(t.priorityLabel ? shortenPriority(t.priorityLabel) : "")) return false;
    if (assigneeFilter.size > 0) {
      const tNameNorm = (t.assigneeName || "").toLowerCase().trim();
      const matches = tNameNorm && [...assigneeFilter].some(f => f.toLowerCase().trim() === tNameNorm);
      if (!matches) return false;
    }
    return true;
  }).sort((a, b) => {
    if (!sortColumn) return 0;

    const aVal = getSortValue(a, sortColumn);
    const bVal = getSortValue(b, sortColumn);

    let comparison = 0;
    if (typeof aVal === "string" && typeof bVal === "string") {
      comparison = aVal.localeCompare(bVal);
    } else if (typeof aVal === "number" && typeof bVal === "number") {
      comparison = aVal - bVal;
    } else {
      comparison = String(aVal).localeCompare(String(bVal));
    }

    return sortDirection === "asc" ? comparison : -comparison;
  });

  useEffect(() => {
    onFilteredCountChange?.(filteredAndSortedTickets.length);
  }, [filteredAndSortedTickets.length, onFilteredCountChange]);

  if (tickets.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center py-24 text-center border rounded-xl border-dashed border-border/60 bg-muted/20">
        <div className="h-12 w-12 rounded-full bg-muted flex items-center justify-center mb-4">
          <Database className="h-5 w-5 text-muted-foreground" />
        </div>
        <h3 data-testid="text-empty-state" className="text-lg font-medium text-foreground">No tickets found</h3>
        <p className="text-sm text-muted-foreground mt-1 max-w-sm">
          No tickets match your current filters.
        </p>
      </div>
    );
  }

  return (
    <>
      <div className="rounded-xl border bg-card overflow-hidden shadow-sm shadow-black/5">
        <div className="overflow-x-auto">
        <Table>
          <TableHeader className="bg-[#163439]">
            <TableRow className="hover:bg-transparent border-b border-white/10">
              <SortableHeader column="isrNumber" label="ISR #" currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort} />
              <TableHead className="w-7 px-1 text-center" />
              <SortableHeader column="title" label="Title" currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort} />
              <SortableHeader
                column="customer" label="Customer"
                currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort}
                filterOptions={getOptions("customer")}
                selectedValues={customerFilter}
                onToggle={makeToggle("customer", customerFilter, setCustomerFilter)}
                onClear={makeClear("customer", setCustomerFilter)}
                extraContent={
                  <button
                    className="ml-0.5 p-1 rounded hover:bg-white/10 transition-colors text-[#F0F5F1]/40 hover:text-[#F0F5F1]/70"
                    onClick={(e) => { e.stopPropagation(); setDirectoryOpen(true); }}
                    data-testid="btn-customer-directory"
                    title="Customer Directory"
                  >
                    <Building2 className="h-3.5 w-3.5" />
                  </button>
                }
              />
              <SortableHeader column="systemId" label="System ID" currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort} />
              <SortableHeader
                column="priority" label="Priority"
                currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort}
                filterOptions={getOptions("priority")}
                selectedValues={priorityFilter}
                onToggle={makeToggle("priority", priorityFilter, setPriorityFilter)}
                onClear={makeClear("priority", setPriorityFilter)}
              />
              <SortableHeader column="hours" label="Age" currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort} />
              <SortableHeader
                column="assignee" label="Assignee"
                currentColumn={sortColumn} sortDirection={sortDirection} onSort={handleSort}
                filterOptions={getOptions("assignee")}
                selectedValues={assigneeFilter}
                onToggle={makeToggle("assignee", assigneeFilter, setAssigneeFilter)}
                onClear={makeClear("assignee", setAssigneeFilter)}
              />
            </TableRow>
          </TableHeader>
          <TableBody>
            {filteredAndSortedTickets.map((ticket) => (
              <TableRow key={ticket.id} data-testid={`row-ticket-${ticket.id}`} className="group transition-colors">
                <TableCell data-testid={`text-ticket-id-${ticket.id}`} className="font-mono text-xs">
                  <button
                    onClick={() => setTimeout(() => setEditingTicket(ticket), 0)}
                    className="text-[#FF9100] hover:underline cursor-pointer font-mono text-xs text-left"
                  >
                    {ticket.ticketNumber || `#${ticket.id}`}
                  </button>
                </TableCell>
                <TableCell className="w-7 px-1 py-1.5">
                  <div className="flex flex-col items-center gap-0.5">
                    <button
                      data-testid={`btn-flag-support-${ticket.id}`}
                      title={ticket.needsSupport ? "Needs support (click to clear)" : "Mark as needs support"}
                      onClick={(e) => { e.stopPropagation(); toggleNeedsSupport(ticket); }}
                      disabled={togglingSupport.has(ticket.id)}
                      className={`w-4 h-4 rounded-sm border flex items-center justify-center transition-colors ${ticket.needsSupport ? "bg-red-600 border-red-600 text-white" : "border-white/20 text-white/20 hover:border-red-500 hover:text-red-400"}`}
                    >
                      <Flag className="h-2.5 w-2.5" />
                    </button>
                    <button
                      data-testid={`btn-parts-needed-${ticket.id}`}
                      title={ticket.partsNeeded ? "Parts needed (click to clear)" : "Mark as parts needed"}
                      onClick={(e) => { e.stopPropagation(); togglePartsNeeded(ticket); }}
                      disabled={togglingParts.has(ticket.id)}
                      className={`w-4 h-4 rounded-sm border flex items-center justify-center transition-colors ${ticket.partsNeeded ? "bg-amber-500 border-amber-500 text-white" : "border-white/20 text-white/20 hover:border-amber-400 hover:text-amber-300"}`}
                    >
                      <Package className="h-2.5 w-2.5" />
                    </button>
                    <div
                      data-testid={`badge-new-ticket-${ticket.id}`}
                      title="Created within the last 48 hours"
                      className={`w-4 h-4 rounded-sm border flex items-center justify-center ${isNewTicket(ticket) ? "bg-blue-500 border-blue-500 text-white" : "border-white/20 text-white/20"}`}
                    >
                      <Sparkles className="h-2.5 w-2.5" />
                    </div>
                  </div>
                </TableCell>
                <TableCell data-testid={`text-ticket-title-${ticket.id}`} className="font-medium text-foreground">
                  <div className="flex items-center gap-1.5 flex-wrap">
                    {ticket.title}
                    {ticket.escalationLevel && ticket.escalationLevel !== "Standard" && ticket.escalationLevel !== "Normal" && (
                      <span
                        data-testid={`badge-escalation-${ticket.id}`}
                        className={`inline-flex items-center px-1.5 py-0 rounded-full border text-[10px] font-semibold ${
                          ticket.escalationLevel === "Critical" ? "bg-red-500/15 text-red-400 border-red-500/40" :
                          ticket.escalationLevel === "High" ? "bg-orange-500/15 text-orange-400 border-orange-500/40" :
                          "bg-amber-500/15 text-amber-400 border-amber-500/40"
                        }`}
                      >
                        {ticket.escalationLevel}
                      </span>
                    )}
                  </div>
                </TableCell>
                <TableCell data-testid={`text-ticket-customer-${ticket.id}`} className="max-w-[180px]">
                  <div className="text-sm text-muted-foreground truncate">{ticket.customerName || "—"}</div>
                  {(() => {
                    const creator = getTicketCreator(ticket);
                    return creator ? (
                      <div className="text-[11px] text-muted-foreground/55 truncate mt-0.5">{sn(creator)}</div>
                    ) : null;
                  })()}
                </TableCell>
                <TableCell data-testid={`text-ticket-system-${ticket.id}`} className="font-mono text-xs text-muted-foreground">
                  {ticket.systemId && ticket.csChannel ? (
                    <a
                      href={`https://app.slack.com/client/T019Y3V5LR4/${ticket.csChannel.replace(/^#/, "")}`}
                      target="_blank"
                      rel="noopener noreferrer"
                      className="text-[#FF9100] hover:underline"
                      onClick={(e) => e.stopPropagation()}
                    >
                      {ticket.systemId}
                    </a>
                  ) : (
                    ticket.systemId || "—"
                  )}
                </TableCell>
                <TableCell data-testid={`text-ticket-priority-${ticket.id}`} className="p-0 pl-4">
                  <Select
                    value={ticket.priorityLabel || "__none__"}
                    disabled={changingPriority.has(ticket.id)}
                    onValueChange={(val) => {
                      handlePriorityChange(ticket, val === "__none__" ? null : val);
                    }}
                  >
                    <SelectTrigger
                      data-testid={`select-priority-${ticket.id}`}
                      className={`h-auto py-1 px-2 border-0 bg-transparent shadow-none focus:ring-0 text-sm font-medium w-auto min-w-[80px] max-w-[140px] ${
                        changingPriority.has(ticket.id) ? "opacity-50" :
                        ticket.priority === "high" ? "text-red-400" :
                        ticket.priority === "low" ? "text-gray-400" :
                        ticket.priorityLabel ? "text-foreground" :
                        "text-muted-foreground"
                      }`}
                    >
                      <SelectValue>
                        {ticket.priorityLabel ? shortenPriority(ticket.priorityLabel) : "—"}
                      </SelectValue>
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value="__none__">— No priority</SelectItem>
                      {(options?.priorityLabels || []).map(p => (
                        <SelectItem key={p} value={p}>{p}</SelectItem>
                      ))}
                    </SelectContent>
                  </Select>
                </TableCell>
                <TableCell data-testid={`text-ticket-hours-${ticket.id}`} className="text-right text-sm text-muted-foreground">
                  {ticket.status === "closed" ? (
                    <div className="flex flex-col items-end leading-tight">
                      <span className="text-[10px] font-medium text-muted-foreground/60 uppercase tracking-wide">Closed</span>
                      <span>{hoursOpen(ticket)}</span>
                    </div>
                  ) : (
                    hoursOpen(ticket)
                  )}
                </TableCell>
                <TableCell data-testid={`text-ticket-assignee-${ticket.id}`} className="text-sm text-muted-foreground">
                  <div className="flex flex-col leading-tight items-center">
                    <span>{sn(ticket.assigneeName)}</span>
                    <span className="text-[10px] text-muted-foreground/60">{timeSinceUpdate(ticket)}</span>
                  </div>
                </TableCell>
              </TableRow>
            ))}
          </TableBody>
        </Table>
        </div>
      </div>

      <Dialog open={!!editingTicket} onOpenChange={(open) => !open && setEditingTicket(null)}>
        <DialogContent
          className="sm:max-w-[720px] max-h-[90vh] overflow-y-auto overflow-x-hidden overscroll-y-contain"
          onFocusOutside={(e) => e.preventDefault()}
        >
          <DialogHeader>
            <DialogTitle className="flex items-center gap-2">
              Edit {editingTicket?.ticketNumber || `Ticket #${editingTicket?.id}`}
              <button
                type="button"
                title="Copy link to this ticket"
                className="ml-1 text-muted-foreground hover:text-foreground transition-colors"
                onClick={() => {
                  const url = `${window.location.origin}/?ticket=${editingTicket?.id}`;
                  navigator.clipboard.writeText(url).then(() => {
                    setCopiedLink(true);
                    setTimeout(() => setCopiedLink(false), 2000);
                  });
                }}
              >
                {copiedLink
                  ? <CopyCheck className="h-3.5 w-3.5 text-green-500" />
                  : <Copy className="h-3.5 w-3.5" />}
              </button>
            </DialogTitle>
          </DialogHeader>
          {editingTicket && (
            <TicketFormErrorBoundary>
              <TicketForm 
                ticket={editingTicket} 
                onSuccess={() => setEditingTicket(null)}
                onCancel={() => setEditingTicket(null)}
              />
            </TicketFormErrorBoundary>
          )}
        </DialogContent>
      </Dialog>

      <CustomerDirectoryDialog
        open={directoryOpen}
        onOpenChange={setDirectoryOpen}
        customerContacts={options?.customerContacts ?? {}}
      />
    </>
  );
}
