function priorityLevelFromLabel(label: string): string {
  const m = label.match(/P(\d+)/i);
  if (!m) return "medium";
  const level = parseInt(m[1]);
  if (level === 1) return "high";
  if (level === 2) return "medium";
  return "low";
}

function shortenPriority(label: string): string {
  const parts = label.split(":");
  if (parts.length >= 2) {
    return parts.slice(0, 2).map(s => s.trim()).join(": ");
  }
  return label;
}

function isAutoGeneratedEntry(text: string): boolean {
  return (
    text.includes("(Auto-Generated)") ||
    text.startsWith("⚠") ||
    text.startsWith("Slack notification sent")
  );
}

function timeSinceUpdate(ticket: Ticket): string {
  // Use the most recent of: createdAt or any *real user* nextStepsHistory entry.
  // Intentionally excludes ticket.updatedAt (bumped by background sync) and
  // auto-generated entries like daily review propagation and Airtable errors.
  const candidates: number[] = [];
  if (ticket.createdAt) candidates.push(new Date(ticket.createdAt).getTime());
  if (ticket.nextStepsHistory?.length) {
    for (const entry of ticket.nextStepsHistory) {
      if (entry.updatedAt && !isAutoGeneratedEntry(entry.text || "")) {
        candidates.push(new Date(entry.updatedAt).getTime());
      }
    }
  }
  if (candidates.length === 0) return "";
  const diffMs = Date.now() - Math.max(...candidates);
  const hours = Math.floor(diffMs / (1000 * 60 * 60));
  if (hours < 1) return "<1H";
  if (hours < 24) return `${hours}H`;
  const days = Math.floor(hours / 24);
  return `${days}D`;
}

function hoursOpen(ticket: Ticket): string {
  const start = ticket.submittedAt || ticket.createdAt;
  if (!start) return "—";
  const startDate = new Date(start);

  let endDate: Date;
  if (ticket.status === "closed" && ticket.resolvedAt) {
    endDate = new Date(ticket.resolvedAt);
  } else if (ticket.status === "closed" && ticket.updatedAt) {
    endDate = new Date(ticket.updatedAt);
  } else {
    endDate = new Date();
  }

  const diffMs = endDate.getTime() - startDate.getTime();
  const hours = Math.round(diffMs / (1000 * 60 * 60));
  if (hours < 1) return "<1h";
  if (hours >= 24) {
    const days = Math.floor(hours / 24);
    const rem = hours % 24;
    return rem > 0 ? `${days}d ${rem}h` : `${days}d`;
  }
  return `${hours}h`;
}

type SortColumn = "isrNumber" | "title" | "customer" | "systemId" | "priority" | "hours" | "assignee" | null;
type SortDirection = "asc" | "desc";

function getSortValue(ticket: Ticket, column: SortColumn): string | number | Date {
  switch (column) {
    case "isrNumber":
      return ticket.ticketNumber || `#${ticket.id}`;
    case "title":
      return ticket.title || "";
    case "customer":
      return ticket.customerName || "";
    case "systemId":
      return ticket.systemId || "";
    case "priority": {
      const match = ticket.priorityLabel?.match(/P(\d+)/i);
      const level = match ? parseInt(match[1]) : 999;
      const subMatch = ticket.priorityLabel?.match(/P\d+-(\d+)/i);
      const sub = subMatch ? parseInt(subMatch[1]) : 0;
      return level * 10 + sub;
    }
    case "hours": {
      const start = ticket.submittedAt || ticket.createdAt;
      if (!start) return 0;
      const startDate = new Date(start);
      let endDate: Date;
      if (ticket.status === "closed" && ticket.resolvedAt) {
        endDate = new Date(ticket.resolvedAt);
      } else if (ticket.status === "closed" && ticket.updatedAt) {
        endDate = new Date(ticket.updatedAt);
      } else {
        endDate = new Date();
      }
      return endDate.getTime() - startDate.getTime();
    }
    case "assignee":
      return ticket.assigneeName || "";
    default:
      return "";
  }
}

